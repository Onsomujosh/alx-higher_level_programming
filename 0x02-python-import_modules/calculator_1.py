#!/usr/bin/python3
def add(a, b):
    """Addition function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a + b
    """
    return (a + b)

def sub(a, b):
    """Subtraction function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a - b
    """
    return (a - b)

def mul(a, b):
    """Multiplication function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a * b
    """
    return (a * b)

def div(a, b):
    """Division function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a / b
    """
    return int(a / b)
